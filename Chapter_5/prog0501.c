#include "stdio.h"



int main(int argc, char const *argv[])
{
	
	for (int i = 0; i < 20; ++i)
	{
		printf("*");
	}

	printf("\nNumeros entre 1 e 5\n");

	for (int i = 0; i < 20; ++i)
	{
		printf("*");
	}	

	printf("\n");

	for (int i = 1; i <= 5; ++i)
	{
		printf("%d\n", i);
	}


	for (int i = 0; i < 20; ++i)
	{
		printf("*");
	}


	printf("\n");
	
	return 0;
}
